#!/bin/bash

for TILESIZE in 128 64 256
do
for BLOCKSIZE in 1 2 4 8 16 32 64 128 256 512 1024 2048 4096
do
for BUFFSIZE in 1 2 4 8 16 32 64 128 256
do

echo RUNNING $TILESIZE $BLOCKSIZE $BUFFSIZE

./run.sh \
-np 8 \
-nt 1 \
-SPATSIZE $TILESIZE \
-SPECSIZE $TILESIZE \
-PROJBLOCK $BLOCKSIZE \
-BACKBLOCK $BLOCKSIZE \
-PROJBUFF $BUFFSIZE \
-BACKBUFF $BUFFSIZE \
ADS1 --intel

done
done
done
